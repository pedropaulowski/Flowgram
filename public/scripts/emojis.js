function mostrarEmojis(nome_array) {
    document.getElementById('emoji-content').innerHTML = ''
    var length = nome_array.length;
    var novo_array = []

    for(var i = 0; i < length; i++) {
        let html = `<a onclick="adicionarEmojiTexto('${nome_array[i]}')" class="emoji-title">${nome_array[i]}</a>`
        novo_array.push(html)
    }

    $('#emoji-content').append(novo_array)
}

function adicionarEmojiTexto(emoji) {
    document.getElementById('message-text').value += emoji
}

